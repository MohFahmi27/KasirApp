    private void masuktable_suku() {
        try{
                sta = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
                tbm = new DefaultTableModel();
                tbm.addColumn("KODE BARANG");
                tbm.addColumn("NAMA BARANG");
                tbm.addColumn("MERK");
                tbm.addColumn("JENIS");
                tbm.addColumn("STOCK");
                tbm.addColumn("HARGA");
                tbm.addColumn("KET");
                
               jTable2.setModel(tbm);
                rs = sta.executeQuery("SELECT suku_cadang.*,merk_barang.id_merk,merk_barang.nm_merk FROM suku_cadang,merk_barang WHERE suku_cadang.id_merk=merk_barang.id_merk AND nm_suku_cadang LIKE '%" +txtcari.getText()+ "%'");
                while (rs.next()){
                    tbm.addRow(new Object[]{
                    rs.getString("kode_barang"),
                    rs.getString("nm_suku_cadang"),
                    rs.getString("nm_merk"),
                    rs.getString("jenis_suku_cadang"),
                    rs.getString("stock"),
                    rs.getString("harga"),
                    rs.getString("ket"),
                    
                    
                });
                    jTable2.setModel(tbm);
                    }
                }catch(Exception e){
                        JOptionPane.showMessageDialog(rootPane,"Gagal"+e);
                        
                    }
    }
    
