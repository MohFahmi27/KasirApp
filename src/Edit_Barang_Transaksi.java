    private void txtqtyKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtqtyKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                String sql = "SELECT harga FROM suku_cadang WHERE kode_barang = '" +txtkode_barang.getText()+ "'";
                rs = sta.executeQuery(sql);
                if (rs.next()) {
                    String harga = rs.getString("harga");
                    int harga1 = Integer.parseInt(harga);
                    int qty = Integer.parseInt(txtqty.getText());

                    int sub_total = qty*harga1;
                    String harga_akhir = String.valueOf(sub_total);
                    txtharga.setText(harga_akhir);
                    txtharga.requestFocus(true);
                }

            } catch (Exception e) {
            }
            //            btntambahActionPerformed(new ActionEvent(evt.getSource(), evt.getID(), "Key Press"+ ""));
        }
    }//GEN-LAST:event_txtqtyKeyPressed

