    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        if (txtnamamerk.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "HARAP ISI SEMUA DATA");
        } else {
            try {
                String sql;
                sql = ("INSERT INTO merk_barang VALUES('" + txtidmerk.getText() + "','" +txtnamamerk.getText()+ "','" + txtketmerk.getText() + "')");
                sta.executeUpdate(sql);

                JOptionPane.showMessageDialog(rootPane, "BERHASIL");

                kosong_merk();
                masuktable_merk();
                this.dispose();
                new Suku_Cadang().setVisible(true);
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btntambahActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        int confirmed = JOptionPane.showConfirmDialog(null,
            "Yakin Ingin Merubah Data", "EDIT DATA",
            JOptionPane.YES_NO_OPTION);
        //dibuat untuk melakukan konfirmasi
        if (confirmed == JOptionPane.YES_OPTION) {
            try {
                String sql;
                sql = ("UPDATE merk_barang set nm_merk='" +txtnamamerk.getText()+ "', "
                    + "ket = '" +txtketmerk.getText()+ "' WHERE id_merk='" +txtidmerk.getText()+ "'");
                sta.executeUpdate(sql);
                JOptionPane.showMessageDialog(rootPane, "BERHASIL DIUBAH");

                kosong_merk();
                masuktable_merk();

                btnedit.setEnabled(false);
                btnhapus.setEnabled(false);
                btntambah.setEnabled(true);

            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btneditActionPerformed

