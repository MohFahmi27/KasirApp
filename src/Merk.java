    private void masuktable_merk() {
        try{
                sta = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
                tbm = new DefaultTableModel();
                tbm.addColumn("ID MERK");
                tbm.addColumn("NAMA MERK");
                tbm.addColumn("KET");
                
                tbl_merk.setModel(tbm);
                rs = sta.executeQuery("SELECT * FROM merk_barang");
                while (rs.next()){
                    tbm.addRow(new Object[]{
                    rs.getString("id_merk"),
                    rs.getString("nm_merk"),
                    rs.getString("ket"),
                    
                    
                });
                    tbl_merk.setModel(tbm);
                    }
                }catch(Exception e){
                        JOptionPane.showMessageDialog(rootPane,"Gagal"+e);
                        
                    }
    }
    
    private void autonomor_merk(){
        String sql = "select max(id_merk) from merk_barang";
        try{
            rs = sta.executeQuery(sql);
            while (rs.next()){
                int a = rs.getInt(1);
                txtidmerk.setText(""+ Integer.toString(a+1));
            }
        }catch (Exception e){
            System.out.println(""+ e.getMessage());
        }

 }
    
    private void kosong_merk() {
        txtnamamerk.setText("");
        txtketmerk.setText("");
        txtnamamerk.requestFocus(true);
        autonomor_merk();
    }

