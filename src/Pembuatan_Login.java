    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        if (txtusername.getText().equals("") ||
            txtpassword.getText().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Isi Semua Data Terlebih Dahulu!");
                
        } else {
            try {
                String sql;
                sql = ("INSERT INTO login VALUES('"+ txtid_login.getText() +"','"+ txtusername.getText() +"','"+ txtpassword.getText() +"','"+ txthak_akses.getText() +"')");
                sta.executeUpdate(sql);
                JOptionPane.showMessageDialog(rootPane, "BERHASIL");
      
                kosong();
                masuktable();
                
                txtusername.requestFocus(true);
                
            } catch (Exception e) {
            }
        } 
        // TODO add your handling code here:
    }//GEN-LAST:event_btntambahActionPerformed

