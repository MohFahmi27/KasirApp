     private void autonomor(){
        String sql = "select max(id_login) from login";
        try{
            rs = sta.executeQuery(sql);
            while (rs.next()){
                int a = rs.getInt(1);
                txtid_login.setText(""+ Integer.toString(a+1));
            }
        }catch (Exception e){
            System.out.println(""+ e.getMessage());
        }

 }
     
     private void kosong() {
         txtusername.setText("");
         txtpassword.setText("");
         autonomor();
     }
 
     private void masuktable() {
        try{
                sta = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
                tbm = new DefaultTableModel();
                tbm.addColumn("ID LOGIN");
                tbm.addColumn("USERNAME");
                tbm.addColumn("LOGIN");
                tbm.addColumn("HAK AKSES");
                
                jTable2.setModel(tbm);
                rs = sta.executeQuery("SELECT * FROM login WHERE hak_akses='Karyawan'");
                while (rs.next()){
                    tbm.addRow(new Object[]{
                    rs.getString("id_login"),
                    rs.getString("username"),
                    rs.getString("password"),
                    rs.getString("hak_akses"),
                    
                    
                });
                    jTable2.setModel(tbm);
                    }
                }catch(Exception e){
                        JOptionPane.showMessageDialog(rootPane,"Gagal"+e);
                        
                    }
    }
     
